        txtrg.setText(bd.getAssistentes().get(indice).getRg());
        txtcpf.setText(bd.getAssistentes().get(indice).getCpf());
        txtemail.setText(bd.getAssistentes().get(indice).getEmail());
        txttelfixo.setText(Integer.toString(bd.getAssistentes().get(indice).getTelFixo()));
        txttelcel.setText(Integer.toString(bd.getAssistentes().get(indice).getTelCel()));
