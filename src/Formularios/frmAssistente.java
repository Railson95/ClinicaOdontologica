    private void cmdbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdbuscarActionPerformed
        
        String codAssistente = JOptionPane.showInputDialog("Favor digitar o código do Assistente: ");
        
        if(codAssistente.equals("")){
            return;
        }
        
        boolean flag = bd.verificaSeAssistenteExiste(Integer.valueOf(codAssistente));
        
        if(!flag){
            JOptionPane.showMessageDialog(rootPane, "Assistente não encontrado");
            return;
        }

        AssistenteAtual = bd.retornaPosAssistente(Integer.valueOf(codAssistente));
        vizualizarAssistente(AssistenteAtual);
    }//GEN-LAST:event_cmdbuscarActionPerformed

